package com.ocp.day04_basic;

public class MultiArrayDemo1 {
    public static void main(String[] args) {
        int[][] m = new int[2][3];
        System.out.println(m.length);
        System.out.println(m);
        System.out.println(m[0]);
        System.out.println(m[0].length);
    }
}
